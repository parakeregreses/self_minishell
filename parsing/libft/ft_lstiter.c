#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst != NULL)
	{
		while (lst->next != NULL)
		{
			if (f != NULL)
				f(lst->content);
			lst = lst->next;
		}
		if (f != NULL)
			f(lst->content);
	}
}
